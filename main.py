@app.post("/webhook/line")
async def line_webhook(request: Request, background_tasks: BackgroundTasks):
    """
    Webhook target URL for the LINE Messaging API.
    Handles verification signatures and forwards processing asynchronous.
    """
    signature = request.headers.get("X-Line-Signature", "")
    body = await request.body()
    body_str = body.decode("utf-8")
    
    base_url = str(request.base_url).rstrip("/")
    if base_url.startswith("http://") and "localhost" not in base_url and "127.0.0.1" not in base_url:
        base_url = "https://" + base_url[7:]
    state.base_url = base_url
    
    if not handler:
        state.add_log("LINE Webhook received a request, but credentials are unconfigured.", "warning")
        return "Webhook received (Bypassed due to empty credentials)"
        
    try:
        background_tasks.add_task(run_line_handler, body_str, signature)
    except Exception as e:
        state.add_log(f"Signature error: {e}", "danger")
        raise HTTPException(status_code=400, detail="Signature authentication failed")
        
    return "OK"

def run_line_handler(body_str: str, signature: str):
    try:
        handler.handle(body_str, signature)
    except InvalidSignatureError:
        state.add_log("Invalid request signature. Event rejected.", "danger")

