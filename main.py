def find_nearest_shelter(lat: float, lon: float, hazard: str = "other") -> dict:
    hazard = hazard.lower()
    
    # Map hazard string to specific hazard types
    target_hazard = "other"
    if "earthquake" in hazard:
        target_hazard = "earthquake"
    elif "fire" in hazard:
        target_hazard = "fire"
    elif "siren" in hazard:
        target_hazard = "siren"
    elif "typhoon" in hazard:
        target_hazard = "typhoon"
        
    best_shelter = None
    min_dist = float('inf')
    
    for s in SHELTERS:
        if target_hazard in s["hazard_types"] or "other" in s["hazard_types"]:
            dist = (lat - s["lat"])**2 + (lon - s["lon"])**2
            if dist < min_dist:
                min_dist = dist
                best_shelter = s
                
    return best_shelter or SHELTERS[0]


# helper function for synchronous trigger of emergency
def trigger_emergency_flow_sync(siren_result):
    state.emergency_mode = True
    state.show_overlay = True
    state.active_hazard = siren_result.get("alert_type", "other")
    state.add_log(f"Emergency detected: {siren_result['alert_type']} (Severity: {siren_result['severity']})", "danger")
    state.add_log(f"Announcement: {siren_result['announcement_summary']}", "danger")
    
    # Generate a default mock route for the simulator so map functions correctly
    shelter = find_nearest_shelter(state.last_latitude, state.last_longitude, state.active_hazard)
    state.route_coordinates = [
        [state.last_latitude, state.last_longitude],
        [(state.last_latitude + shelter["lat"])/2.0, (state.last_longitude + shelter["lon"])/2.0],
        [shelter["lat"], shelter["lon"]]
    ]
    state.route_steps = shelter["steps"]
    state.add_log(f"Nearest shelter identified: {shelter['name']}. Evacuation route generated.", "success")
    state.add_log("Emergency warning: Sent urgent alert to Hana Yamada's family.", "danger")

