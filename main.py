def generate_safety_card_pillow(shelter_name: str, hazard_type: str, steps: list) -> bytes:
    # Create a 600x600 image with a warm alert background
    img = Image.new("RGB", (600, 600), "#fff5f5")
    draw = ImageDraw.Draw(img)
    
    # Draw top alert banner
    draw.rectangle([(0, 0), (600, 80)], fill="#ff4757")
    
    # Try to load fonts safely
    font_title = None
    font_body = None
    font_bold = None
    
    font_paths = [
        "/System/Library/Fonts/Helvetica.ttc",
        "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf",
        "/usr/share/fonts/truetype/freefont/FreeSansBold.ttf"
    ]
    for path in font_paths:
        if os.path.exists(path):
            try:
                font_title = ImageFont.truetype(path, 22)
                font_body = ImageFont.truetype(path, 15)
                font_bold = ImageFont.truetype(path, 17)
                break
            except:
                pass
                
    if not font_title:
        font_title = ImageFont.load_default()
        font_body = ImageFont.load_default()
        font_bold = ImageFont.load_default()
        
    draw.text((20, 28), "⚠️ EMERGENCY SAFETY ROUTE", fill="#ffffff", font=font_title)
    
    # Draw Hazard Type
    draw.text((30, 110), "🚨 DETECTED HAZARD:", fill="#2f3542", font=font_bold)
    draw.text((35, 138), hazard_type.upper(), fill="#ff4757", font=font_title)
    
    # Draw Shelter Info Box
    draw.rectangle([(30, 190), (570, 285)], fill="#e3faf2", outline="#2ed573", width=2)
    draw.text((50, 205), "📍 ASSIGNED SHELTER:", fill="#2ed573", font=font_bold)
    draw.text((50, 238), shelter_name, fill="#2f3542", font=font_title)
    
    # Draw Steps
    draw.text((30, 310), "🏃 WALKING DIRECTIONS:", fill="#2f3542", font=font_bold)
    y = 345
    for idx, step in enumerate(steps[:3]):
        words = step.split()
        current_line = []
        lines = []
        for word in words:
            if len(" ".join(current_line + [word])) < 50:
                current_line.append(word)
            else:
                lines.append(" ".join(current_line))
                current_line = [word]
        lines.append(" ".join(current_line))
        
        for line_idx, line in enumerate(lines):
            prefix = f"{idx + 1}. " if line_idx == 0 else "   "
            draw.text((45, y), f"{prefix}{line}", fill="#57606f", font=font_body)
            y += 20
        y += 8
        
    # Draw Footer
    draw.text((30, 545), "Shared automatically with your caregiver network.", fill="#a4b0be", font=font_body)
    
    # Save to bytes
    out = io.BytesIO()
    img.save(out, format="PNG")
    return out.getvalue()

def find_dynamic_shelter_via_gemini(lat: float, lon: float, address: str, hazard: str) -> dict:
    """
    Uses Gemini to dynamically identify a real, local evacuation center or public park
    suitable for the active hazard, close to the user's live coordinates.
    """
    if agents.IS_OFFLINE:
        return {
            "name": "Local District Safety Shelter",
            "lat": lat + 0.002,
            "lon": lon + 0.002,
            "steps": [
                "Proceed north toward the main main crossing.",
                "Turn right and walk 200m to the designated municipal shelter."
            ]
        }
        
    try:
        prompt = f"""
        The user is located at coordinates ({lat}, {lon}) which corresponds to the address: "{address}".
        An active emergency hazard of type "{hazard}" has been detected.
        
        Identify a real, suitable emergency evacuation shelter, public park, or safety zone near this location.
        Return a JSON object in English with the following keys.
        Do not wrap it in markdown like ```json or ```.
        
        JSON Keys:
        - name: The name of the real shelter (e.g. "Mito City Joto Elementary School" or "Shinjuku Gyoen National Garden")
        - lat: Estimated latitude of the shelter (should be near the user's coordinates, e.g. within 1-2 km)
        - lon: Estimated longitude of the shelter (should be near the user's coordinates, e.g. within 1-2 km)
        - steps: A list of 2-3 walking step directions from the user's location to the shelter.
        """
        
        model = agents.genai.GenerativeModel("models/gemini-3.5-flash")
        response = model.generate_content(prompt)
        text = response.text.strip()
        if text.startswith("```"):
            text = text.split("```")[1]
            if text.startswith("json"):
                text = text[4:]
                
        return json.loads(text.strip())
    except Exception as e:
        print(f"[ERROR] Dynamic shelter generation failed: {e}")
        # Fallback to Shibuya Jinnan
        return {
            "name": "Shibuya Jinnan Elementary School",
            "lat": 35.6631,
            "lon": 139.7001,
            "steps": [
                "Proceed toward Shibuya Jinnan area.",
                "Head to Jinnan Elementary School."
            ]
        }

